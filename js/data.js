export function initGrille() {
    const grille = new Array(HAUTEUR_GRILLE);
    for (let y = 0; y < HAUTEUR_GRILLE; y++) {
        grille[y] = new Array(LARGEUR_GRILLE);
        for (let x = 0; x < LARGEUR_GRILLE; x++) {
            grille[y][x] = -1;
        }
    }
    return grille;
}
